# Estruturas de Repetição

x = 0
while ( x< 10 ):
    print(x,end='; ')
    x = x+1
print()
for y in range(1,10,1):
    print(y)

frutas = ['abacate', 'abacaxi', 'maçã', 'banana', 'pêssego']
for fruta in frutas:
    print(fruta, end=' - ')

alunos = ['aluno_1', 'aluno_2', 'aluno_3', 'aluno_4', 'aluno_5']
for aluno in alunos:
    print(aluno, end=', ')
