# Extruturas de repetição

x = 0
while (x < 10):
    print(x, end='; ')
    x = x+1
print()

for y in range(1,10,1):
    print(y)

frutas = ['abacate', 'abacaxi', 'abóbora', 'banana', 'maçã']
for fruta in frutas:
    print(fruta)


